#include "hero.h"

void Hero::attack(DIR d)
{
	switch(d)
	{
	case UP:
	case DOWN:
	case LEFT:
	case RIGHT:
	case UP_RIGHT:
	case UP_LEFT:
	case DOWN_RIGHT:
	case DOWN_LEFT:
	}

}	


void Hero::move(DIR d)
{
	switch(d)
	{
	case UP:
	case DOWN:
	case LEFT:
	case RIGHT:
	case UP_RIGHT:
	case UP_LEFT:
	case DOWN_RIGHT:
	case DOWN_LEFT:
	}

}
